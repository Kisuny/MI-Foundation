function add_apothecary_recipe(event, item) {
    let time = item.time ? item.time : 200
    let cost = item.cost ? item.cost : 16
    event = event.recipes.modern_industrialization.apothecary_chamber(cost, time)

    if (item.fluid) {
        item.fluid.forEach(element => {
            event = event.fluidIn(element[0], element[1])
        });   
    }
    if (item.seeds === undefined || item.seeds === true) {
        event = event.itemIn("minecraft:wheat_seeds")   
    }
    if (item.items_in) {
        item.items_in.forEach(element => {
            event = event.itemIn(element[0], element[1] !== undefined ? element[1] : 1)
        });
    }
    if (item.items_out) {
        item.items_out.forEach(element => {
            event = event.itemOut(element[0], element[1] !== undefined ? element[1] : 1)
        });
    }
    
    event = event.fluidIn("minecraft:water", 1000) 
}


ServerEvents.recipes(event => {
    apothecary_recipes.forEach(element => {
        add_apothecary_recipe(event, element)
    });
})
function addBags(event, lootbagType, object) {
    for (let rarity of global.lootbagRarity) {
        event
            .addLootTableModifier(`lootbag:${lootbagType}/${rarity}`)
            .addWeightedLoot(
                object[rarity].itemQuantity,
                object[rarity].lootPool,
            )
    }
};

LootJS.modifiers((event) => {
    addBags(event, 'food', food)
    addBags(event, 'mob', mob)
    addBags(event, 'magic', magic)
    addBags(event, 'early', early)
    addBags(event, 'mid', mid)
    addBags(event, 'late', late)
    addBags(event, 'end', end)
});

function ItemOfBag(name, quantity, itemChance) {
    this.itemName = name;
    this.itemChance = itemChance || 5;
    this.quantity = quantity || 1;

    this.createItem = () => Item.of(`${this.quantity}x ${this.itemName}`).withChance(this.itemChance)
};

function Bag(lootPool, itemQuantity) {
    this.lootPool = lootPool.map(item => item.createItem());
    this.itemQuantity = itemQuantity || 1;
};
